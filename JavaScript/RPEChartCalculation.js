function calculateE1RM(reps, rpe, weightKg) {
    rpe = parseFloat(rpe); // Ensure RPE is a number
    if (rpeChart[reps] && rpeChart[reps][rpe]) {
        const e1rmPercentage = rpeChart[reps][rpe];
        const e1rmValue = weightKg / (e1rmPercentage / 100);
        return e1rmValue;
    } else {
        return null; // Return null if the reps or rpe are not in the chart
    }
}
